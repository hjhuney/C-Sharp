using System;
public class Variables
{
    static public void Main ()
    {
        int number = 5;
        int aVal = 7;
        float myFloat = 12.765f;
        char myCharacter = 'A';
        Console.WriteLine ("My values are {0}, {1}, {2}", number, aVal, myFloat);
        Console.WriteLine ("My char is: {0}", myCharacter);
    }
}